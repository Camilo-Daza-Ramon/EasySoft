    public function traer_municipios(Request $request){
        if ($request->ajax()) {

            $municipios = Municipio::selectRaw('MunicipioId as id, NombreMunicipio as nombre')->where([['DeptId', $request->departamento_id]])->orderBy('nombre', 'ASC')->get(); 
            return response()->json($municipios);
        }  
